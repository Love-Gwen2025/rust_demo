// ============================================
// Demo 01: 变量与可变性
// 运行: cargo run --example 01_variables
// ============================================

fn main() {
    // ---------- 不可变变量 ----------
    let x = 5;
    println!("x 的值是: {}", x);
    // x = 6;  // 错误！不可变变量不能重新赋值

    // ---------- 可变变量 ----------
    let mut y = 10;
    println!("y 的初始值: {}", y);
    y = 20; // 可以重新赋值
    println!("y 的新值: {}", y);

    // ---------- 变量遮蔽 (Shadowing) ----------
    let z = 100;
    let z = z + 1; // 可以用 let 重新声明同名变量
    let z = z * 2;
    println!("z 的最终值: {}", z); // 202

    // ---------- 常量 ----------
    const MAX_POINTS: u32 = 100_000; // 必须标注类型，命名用大写
    println!("最大点数: {}", MAX_POINTS);
}
